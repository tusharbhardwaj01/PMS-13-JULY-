              Shri Gopal Bhushan is appointed as the Director of Defence Scientific Information and Documentation Center (DESIDOC) with effect from 30 June 2015. Prior to this appointment, Shri Bhushan was the Director for International Cooperation (DIC) at DRDO Headquarters - the position he took over on 1st Jan, 2010.<br><br>
 As DIC, Shri Bhushan pursued DRDOs international affairs with foreign countries at the governmental level and represented DRDO in various multilateral forums and outreach meetings on disarmament and international security. He has a vast experience of working with various Department/ Minstries of Government of India and international defence research laboratories, military organizations, defence industry and foreign academia.He worked with foreign exports regulatory authorities on resolving export/trade restrictions and impediments imposed on DRDO.He provided a regular insight on prevailing geo-political situations, strategic alliances, current affairs, and bilateral and multilateral activities to the top management. Throughout, Shri Bhushan has been guided by pragmatism and his belief in the boundless opportunities.He worked tirelessly to conrtibute to the ideas, policies, and innovations that assured widening of DRDO's International defence R & D Cooperation. Due to his momentous efforts, DRDO's International defence R & D Cooperation is seeing tremendous growth and expansion with greater openness.There is a revival of interest in DRDO by the international defence community.<br><br>
 
    In his career since 1986, he has served in DRDO in a variety of positions. From April 1986 to July 1997, he worked as a software engineer at DRDO Computer Centre and at the Directorate of Advanced Computing and Systems Analysis. In July 1997, he joined Institute of Systems Studies and Analyses (ISSA) where he was involved with the development of wargame software for the Indian Army. In May 2000, he was appointed as the Staff Officer to SA to RM which followed his appointment as Adviser (Defence Technology) at the Embassy of India in Washington D.C., USA. As a diplomat, he served in the Indian Mission from July 2006 to Oct, 2009. On his return, he was appointed, the Officer on Special duty (International Affairs) to SA to RM. On 1st Jan, 2010 he took over as the Director for International Affairs at DRDO HQrs.<br><br>

